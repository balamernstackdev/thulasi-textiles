import { NextResponse } from 'next/server';
import crypto from 'crypto';
import prismadb from '@/lib/prisma';
import { sendEmail } from '@/lib/mail';
import { getOrderConfirmationTemplate } from '@/lib/mail-templates';

export async function POST(req: Request) {
    try {
        const {
            razorpay_order_id,
            razorpay_payment_id,
            razorpay_signature,
            orderId // Our internal DB order ID
        } = await req.json();

        const body = razorpay_order_id + "|" + razorpay_payment_id;

        const expectedSignature = crypto
            .createHmac("sha256", process.env.RAZORPAY_KEY_SECRET!)
            .update(body.toString())
            .digest("hex");

        const isAuthentic = expectedSignature === razorpay_signature;

        if (isAuthentic) {
            // Payment Success - Update Database

            if (orderId) {
                const order = await prismadb.order.update({
                    where: { id: orderId },
                    data: {
                        paymentStatus: 'PAID',
                        paymentId: razorpay_payment_id,
                        status: 'PROCESSING' // Move from PENDING to PROCESSING automatically on payment
                    },
                    include: {
                        user: true,
                        address: true,
                        items: {
                            include: {
                                variant: {
                                    include: {
                                        product: true
                                    }
                                }
                            }
                        }
                    }
                });

                // Send Confirmation Email
                if (order.user?.email) {
                    await sendEmail({
                        to: order.user.email,
                        subject: `Order Confirmed: #${order.id.slice(-6).toUpperCase()}`,
                        html: getOrderConfirmationTemplate(order)
                    });
                }
            }

            return NextResponse.json({
                success: true,
                message: "Payment verified successfully"
            });
        } else {
            return NextResponse.json({
                success: false,
                message: "Invalid signature"
            }, { status: 400 });
        }

    } catch (error) {
        console.error('Payment Verification Error:', error);
        return NextResponse.json({ error: 'Error verifying payment' }, { status: 500 });
    }
}
